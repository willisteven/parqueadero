package com.ceiba.parqueadero.objetosnegocio;


public class VehiculosParqueaderoNegocio {

	private String ingresoFecha;
	private String tipo;
	private String placa;

	public String getIngresoFecha() {
		return ingresoFecha;
	}

	public void setIngresoFecha(String ingresoFecha) {
		this.ingresoFecha = ingresoFecha;
	}

	public String getTipo() {
		return tipo;
	}

	public void setTipo(String tipo) {
		this.tipo = tipo;
	}

	public String getPlaca() {
		return placa;
	}

	public void setPlaca(String placa) {
		this.placa = placa;
	}

}
